package com.example.myrecyclerviewtest;

public class Actor {
    private String name;
    private int imageId;

    public Actor(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
